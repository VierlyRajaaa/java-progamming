package operator_pemograman;
//untuk menggabungkan dua masa
public class operatorAritmatika {
    //untuk menjumlahkan nilai
    public static void main(String[] args) {
        //untuk menghasilkan bahasa pemograman

        int angka1 =10;
        //int angka1;mendeklarasikan angka 1
        int angka2 =5;
        //untuk mendeklarasikan angka 2
        int hasil;
        // hasil dari 10

        // Penjumlahan
        //penjumlahan dari programan
        hasil = angka1 + angka2;
        // mendeklarasikan angka1 dan angka2
        System.out.println("Hasil penjumlahan = " + hasil);
        //untuk menjumlahkan nilai

        // Perkalian
        //untuk perkalian logaritme
        hasil = angka1  - angka2;
        //mendeklarasikan angka1 dan angka2
        System.out.println("Hasil pengurangan =" + hasil);
        //hasil dari angka1 dan angka 2 109

        // Perkalian
        // perkalian tersebut dua
        hasil = angka1 * angka2;
        // mendeklarasikan angka1 dan angka2
        System.out.println("Hasil perkalian =" + hasil);
        //menjumlahkan dari angka 1 dan angka2

        // Pembagian
        //pembagian dari kurang 2
        hasil = angka1 / angka2;
        //untuk menjumlahkan dari 5
        System.out.println("Hasil pembagian =" + hasil);
        // untuk menaruh sebuah wadah

        // Modulus
        //untuk menjumlahkan dan perkurangan
        hasil = angka1 % angka2;
        //untuk menghasilkan logaritme
        System.out.println("Hasil modulus=" + hasil);
        //untuk perkurangan dan pertambahan
    } 
}
